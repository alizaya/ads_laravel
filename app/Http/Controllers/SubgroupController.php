     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index($id)
    {

        $group = Group::findorfail($id);
        $subgroup = $group->subgroups;
        return view('group.subgroupindex', ['subgroup' => $subgroup, 'group' => $group]);

